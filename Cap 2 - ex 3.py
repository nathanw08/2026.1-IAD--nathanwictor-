# Declaração das variáveis
Largura = 17
Altura = 12.0

# 1. Divisão inteira
resultado1 = Largura // 2
print(f"1. Valor: {resultado1}, Tipo: {type(resultado1)}")

# 2. Divisão por ponto flutuante
resultado2 = Largura / 2.0
print(f"2. Valor: {resultado2}, Tipo: {type(resultado2)}")

# 3. Divisão de um float por int
resultado3 = Altura / 3
print(f"3. Valor: {resultado3}, Tipo: {type(resultado3)}")

# 4. Expressão aritmética com precedência
resultado4 = 1 + 2 * 5
print(f"4. Valor: {resultado4}, Tipo: {type(resultado4)}")